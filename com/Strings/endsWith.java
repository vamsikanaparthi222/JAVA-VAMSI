package com.Strings;

//endsWith means a particular letter is matching or not in our word
public class endsWith {

	public static void main(String[] args) {
		String s1="Hello";
		String s2="hello";
		String s3="HELLO";
		
		System.out.println(s1.endsWith("lo"));
		System.out.println(s2.endsWith("Lo"));
		System.out.println(s3.endsWith("LO"));
		
		System.out.println(s1.endsWith("lo"));
		System.out.println(s2.endsWith("Lo"));
		System.out.println(s3.endsWith("LO"));
		
		System.out.println(s1.endsWith("lo"));
		System.out.println(s2.endsWith("Lo"));
		System.out.println(s3.endsWith("LO"));
	}

}
