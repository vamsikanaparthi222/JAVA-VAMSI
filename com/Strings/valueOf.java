package com.Strings;

//means it converts String value to integer value
// 			or
//convert string (or) wrapper object to primitive data types
//it returns string to integer value
public class valueOf {

	public static void main(String[] args) {
		String s1="Hello";
		String s2="222";
		
		System.out.println(s1.valueOf("H"));
		System.out.println(s2.valueOf(s2));
		System.out.println(s1.valueOf(s1));
		

	}

}
