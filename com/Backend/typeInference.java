package com.Backend;

//typeInference Means compiler infers the type of variables using value provided
//type inference is restricted to local variable
//this typeInference is used var keyword
//it automatically choose what is that type
//instead of data type we can use var keyword
public class typeInference {

	public static void main(String[] args) {
		var i=222;
		int j=196;
		System.out.println(i);
		System.out.println(j);
	}

}
